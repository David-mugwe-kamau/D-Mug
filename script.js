// Parallax Effect
let ticking = false;
const parallaxContainer = document.querySelector('.parallax-bg');

function updateParallax() {
    const scrollTop = window.pageYOffset || document.documentElement.scrollTop;
    if (parallaxContainer) {
        const speed = 0.15;
        // Use a subtle parallax effect that doesn't move too much
        const offset = scrollTop * speed;
        parallaxContainer.style.transform = `translate3d(0, ${offset}px, 0)`;
    }
    ticking = false;
}

window.addEventListener('scroll', () => {
    if (!ticking) {
        window.requestAnimationFrame(updateParallax);
        ticking = true;
    }
}, { passive: true });

// Ensure background is properly positioned on load
window.addEventListener('load', () => {
    if (parallaxContainer) {
        parallaxContainer.style.transform = 'translate3d(0, 0, 0)';
    }
});

// Fade-in animations on scroll
const observerOptions = {
    threshold: 0.1,
    rootMargin: '0px 0px -50px 0px'
};

const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('fade-in-visible');
            observer.unobserve(entry.target);
        }
    });
}, observerOptions);

// Observe all sections
document.addEventListener('DOMContentLoaded', () => {
    const sections = document.querySelectorAll('.hero, .content, .skills, .projects, .contact');
    sections.forEach(section => {
        section.classList.add('fade-in');
        observer.observe(section);
    });
});

// Scroll to Top Button
const scrollTopBtn = document.getElementById('scroll-top');

window.addEventListener('scroll', () => {
    if (window.pageYOffset > 300) {
        scrollTopBtn.classList.add('show');
    } else {
        scrollTopBtn.classList.remove('show');
    }
});

scrollTopBtn.addEventListener('click', () => {
    window.scrollTo({
        top: 0,
        behavior: 'smooth'
    });
});

